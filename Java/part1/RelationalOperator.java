
class RelationalOperator
{
   public static void main(String [] args)
   {
       char a='A';
       char b='B';
       System.out.println(a>b);
       System.out.println(a<b);
       System.out.println(a==b);
       System.out.println(a>=b);
       System.out.println(b<=a);
       System.out.println(a!=b);
       System.out.println('A'>'B');
       System.out.println('A'=='B');
       System.out.println('A'!='B');
       System.out.println('A'>='B');
       System.out.println('A'<='B');
       System.out.println('A'<'B');
   }
}
