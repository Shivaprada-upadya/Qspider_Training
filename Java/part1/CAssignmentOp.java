class CAssignmentOp
{
    public static void main(String[] args)
    {
        int a=5;
        int b=30;
        int c=50;
        a+=5;
        System.out.println(a); //10
        b-=2;
        System.out.println(b); //28
        c*=1;
        System.out.println(c); //50
        a%=2;
        System.out.println(a); //0
        c/=5;
        System.out.println(c); //10
    }
    
}